public class QuadrilateralArea{
    public static void main(String[] args){
        int diagonal = 46;
        int height1 = 13;
        int height2 = 10;
        int area = 50 * diagonal * (height1 + height2 ) / 10;
        System.out.println("the area of quadrilateral is : " + area + "square meters ");
    }
}